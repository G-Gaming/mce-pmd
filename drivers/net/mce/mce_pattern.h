#define MCE_PTYPE_GTP_U_INNER_IPV6_FRAG	  (0b000101)
#define MCE_PTYPE_GTP_U_INNER_IPV6_PAY	  (0b011100)
#define MCE_PTYPE_GTP_U_INNER_IPV6_TCP	  (0b011101)
#define MCE_PTYPE_GTP_U_INNER_IPV6_UDP	  (0b011110)
